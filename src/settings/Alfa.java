package settings;

public class Alfa {
    private final double alfaX;
    private final double alfaY;
    private final double alfaZ;

    public Alfa(double alfaX, double alfaY, double alfaZ) {
        this.alfaX = alfaX;
        this.alfaY = alfaY;
        this.alfaZ = alfaZ;
    }

    public double getAlfaX() {
        return alfaX;
    }

    public double getAlfaY() {
        return alfaY;
    }

    public double getAlfaZ() {
        return alfaZ;
    }
}
